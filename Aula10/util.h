double diametro(double raio){
    double diametro;
    diametro = raio * 2;
    return diametro;
}

double circunferencia(double raio){
    double circunferencia;
    circunferencia = (3.1415 * 2) * raio;
    return circunferencia;
}

double area(double raio){
    double area;
    area = 3.1415 * (raio * raio);
    return area;
}
